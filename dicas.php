        <h1>Por que evitar o desperdício de comida?</h1>
        <p>Toda vez que desperdiçamos alimentos, desperdiçamos também trabalho, esforço, investimento e recursos naturais (como água, energia, sementes, etc.) essenciais para a sua produção, transporte e processamento. O desperdício de alimentos, portanto, envolve processos que emitem grande quantidade de gases de efeito estufa, agravando as Mudanças Climáticas.

            Além disso, só no Brasil descartamos 26,3 milhões de toneladas de alimentos por ano, enquanto 14 milhões de brasileiros passam fome. Este total desperdiçado poderia satisfazer as necessidades nutricionais de 11 milhões de pessoas.

            Para conscientizar sobre a importância de evitar o desperdício e a perda de alimentos, a Organização das Nações Unidas para a Alimentação e a Agricultura (FAO) lançou em setembro a Plataforma Técnica de Medição e Redução de Perda e do Desperdício de Alimentos ( http://www.fao.org/platform-food-loss-waste/food-waste/introduction/en/ ), que reúne dados e informações sobre políticas, ações, exemplos de cases de sucesso na redução do desperdício e dicas de hábitos de consumo consciente que cada um de nós pode adotar.

            Dentre as recomendações, estão:

            Adote uma dieta mais saudável e sustentável: Prefira ingredientes orgânicos e de pequenos produtores; aposte em receitas balanceadas e que fazem o uso integral dos alimentos
            Compre apenas o que você precisa: Planeje suas refeições; faça uma lista de compras e cumpra-a, evitando compras por impulso. Assim, você evita o desperdício de alimentos e economiza dinheiro!
            Escolha frutas e vegetais “feios”: Não julgue os alimentos pela aparência. Frutas e vegetais com formatos estranhos ou machucados têm o mesmo gosto e os mesmos nutrientes.
            Armazene corretamente os alimentos: Deixe produtos antigos na frente do armário ou geladeira e os novos no fundo para utilizá-los dentro da data de validade e não perdê-los. Use recipientes para manter alimentos abertos e frescos.
            Leia os rótulos: Conheça os ingredientes que compõem o alimento, evite produtos com alto teor de gorduras trans, conservantes, açúcar ou sódio. Verifique também a data de validade e a maneira correta de conservá-lo.
            Ame suas sobras: Se não comer tudo o que preparou hoje, use as sobras como ingrediente na refeição seguinte ou congele para utilizá-la posteriormente.

